package DSA_Basics;

public class Arrays {
    public static void main(String[] args) {
        // type[] arrayName = new type[size];-->syntax
        int[] marks = new int[20];
        //indexing starts from 0
        marks[0]=97;
        marks[1]=98;
        marks[2]=95;
        for (int i = 0; i <3 ; i++) {
            System.out.println(marks[i]);
        }
//        int number[] = {97,98,95};
        /*if we are taking inputs from the user and if there is no input then the default value
        inside array is zero*/

    }
}
